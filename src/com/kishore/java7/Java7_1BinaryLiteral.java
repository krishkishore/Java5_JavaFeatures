package com.kishore.java7;

public class Java7_1BinaryLiteral {
	public static void main(String[] args) {

		int i1 = 0b1101;
		int i2 = 0B1101;

		System.out.println("----------Binary Literal in Integer----------------");
		System.out.println("i1 = " + i1);
		System.out.println("i2 = " + i2);

	}
}
